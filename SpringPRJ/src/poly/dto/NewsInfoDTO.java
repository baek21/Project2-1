package poly.dto;

public class NewsInfoDTO {
	private String newsReg;
	private String newsArea;
	private String newsTitle;
	private String newsContents;
	public String getNewsReg() {
		return newsReg;
	}
	public void setNewsReg(String newsReg) {
		this.newsReg = newsReg;
	}
	public String getNewsArea() {
		return newsArea;
	}
	public void setNewsArea(String newsArea) {
		this.newsArea = newsArea;
	}
	public String getNewsTitle() {
		return newsTitle;
	}
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	public String getNewsContents() {
		return newsContents;
	}
	public void setNewsContents(String newsContents) {
		this.newsContents = newsContents;
	}
	
	
}
